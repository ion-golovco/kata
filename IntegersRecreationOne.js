function sqrdividers(n) {
    let dividere = [];
    for (let j = 1; j <= n; j += 1) {
        if (n % j === 0) {
            dividere.push(Math.pow(j, 2))
        }
    }
    if (dividere.length > 0) {
        return dividere.reduce((a, b) => a + b)
    }
    return dividere
}
function listSquared(m, n) {
    if (m == 0 || n == 0) { return false }
    let res = []
    for (i = m; i < n; i += 1) {
        if (Math.sqrt(sqrdividers(i)) % 1 == 0) {
            res.push([i, sqrdividers(i)])
        }
    }
    return res
}
